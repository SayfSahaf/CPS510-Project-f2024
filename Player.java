public class Player {

    int playerID;
    String playerName;
    int playerAge;
    int playerJersey;
    String playerPosition;
    String nationality;

    public Player(
        int playerID,
        String playerName,
        int playerAge,
        int playerJersey,
        String playerPosition,
        String nationality
    ) {
        this.playerID = playerID;
        this.playerName = playerName;
        this.playerAge = playerAge;
        this.playerJersey = playerJersey;
        this.playerPosition = playerPosition;
        this.nationality = nationality;
    }
}
